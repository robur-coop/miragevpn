module Lzo_crowbar = struct
  let decompress () =
    Printexc.record_backtrace false;
    Crowbar.add_test ~name:"[Crowbar.string] invalid input doesn't crash"
      [ Crowbar.bytes ] (fun s -> match Lzo.decompress s with _ -> ());
    Printexc.record_backtrace true

  let lzo_suite = [ ("Lzo.decompress", `Slow, decompress) ]
end

