     Alcotest.(check (result string_enc reject_err))
       "literal run of 3+15+33151 bytes, overflows (2**15)-1" (Ok many_As)
       (Lzo.decompress
          ("\000" ^ String.make 130 '\x00' ^ "\x01" ^ many_As ^ "\x11\x00\x00")));
    (let many_As = String.make (3 + 15 + 76501) 'A' in
     Alcotest.(check (result string_enc reject_err))
       "literal run of 3+15+76501 bytes, overflows (2**16)-1" (Ok many_As)
       (Lzo.decompress
          ("\000" ^ String.make 300 '\x00' ^ "\x01" ^ many_As ^ "\x11\x00\x00")));
    (* Well, we're just going to expect these to fail since
       inputs > 1GB are not really feasible/realistic.
       Instead of verifying that it decodes correctly we verify that it fails
       with the expected (stringly typed) error:
    *)
    Alcotest.(check (result reject alco_msg))
      "literal run of 1073742016 bytes, overflows (2**30)-1"
      (Error (`Msg "Malformed input"))
      (Lzo.decompress
         ("\000" ^ String.make 4210753 '\x00' ^ "\x01\x11\x00\x00"));
    Alcotest.(check (result reject alco_msg))
      "literal run of 2147483776 bytes, overflows (2**31)-1"
      (Error (`Msg "Malformed input"))
      (Lzo.decompress
         ("\000" ^ String.make 8421505 '\x00' ^ "\x01\x11\x00\x00"));
    Alcotest.(check (result reject alco_msg))
      "literal run of 4295297776 bytes, overflows (2**32)-1"
      (Error (`Msg "Malformed input"))
      (Lzo.decompress
         ("\000" ^ String.make 16844305 '\x00' ^ "\x01\x11\x00\x00"))

  let lzo_suite : _ Alcotest.test_case list =
    [
