  Logs.set_reporter @@ Logs_fmt.reporter ~dst:Format.std_formatter ();
  (*Logs.(set_level @@ Some Debug);*)
  Alcotest.run "openvpn LZO decompression tests"
    [ ("Hardcoded", Lzo_tests.lzo_suite); ("AFL", Lzo_crowbar.lzo_suite) ]
